package com.techlabs.test;

public class Box {

	private int height;
	private int depth;
	private int width;
	
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getDepth() {
		return depth;
	}
	public void setDepth(int depth) {
		this.depth = depth;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public Box(int height, int depth, int width) {
		super();
		this.height = height;
		this.depth = depth;
		this.width = width;
	}
	
	
	
}
